                                        <h6>Que es una memoria RAM?</h6>
                                        La memoria RAM es la memoria principal de un dispositivo, esa donde se almacenan de forma temporal los datos de los
                                        programas que estás utilizando en este momento. Sus siglas significan Random Access Memory, lo que traducido al español
                                        sería Memoria de Acceso Aleatorio, y es un tipo de memoria que te puedes encontrar en cualquier dispositivo, desde ordenadores
                                        de sobremesa hasta teléfonos móviles.
                                        La memoria RAM tiene dos características que la diferencian del resto de tipos de almacenamiento. Por una parte tiene una enorme velocidad, y por otra los datos sólo se almacenan de forma temporal.
                                        Esto quiere decir que cada vez que reinicies o apagues tu ordenador, lo normal es que los datos que haya almacenados en la RAM se pierdan.
                                        <h6>Para que sirve una memoria RAM</h6>
                                        Tu ordenador o móvil no ejecuta todas las acciones utilizando únicamente el disco duro, ya que si lo hiciera tardaría demasiado en ejecutarlas. Por eso, se utiliza un tipo de memoria mucho más rápida para hacer estas tareas más inmediatas, y es la encargada de almacenar las instrucciones de la CPU o los datos que las aplicaciones necesitan constantemente. Estas instrucciones quedan allí hasta que se apague el ordenador o hasta que se sustituyan por otros nuevos.

                                            Para que lo entiendas, vamos a imaginarnos que estás trabajando en tu escritorio. Los cajones son tu disco duro, donde almacenas todo lo que puedes utilizar. Pero también necesitas poner en el propio escritorio las herramientas y archivos que estás utilizando,
                                            y eso es lo que haría en este caso la memoria RAM. Así no tienes que estar abriendo y cerrando los cajones, lo tienes todo en frente.
